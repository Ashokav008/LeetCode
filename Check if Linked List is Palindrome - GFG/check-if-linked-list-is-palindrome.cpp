class Solution{
  public:
    //Function to check whether the list is palindrome.
  
       
    bool isPalindrome(Node *head)
    {
        
        string a="";
        bool ans=true;
        while(head){
            a.push_back(char(head->data));
            head=head->next;
        }
       int j=a.size()-1;
      int i=0;
       while(i<j){
           if(a[i]!=a[j])
                {
                    ans=false;
                    break;
                }
                i++;
                j--;
       }
        return ans;
    }
};



